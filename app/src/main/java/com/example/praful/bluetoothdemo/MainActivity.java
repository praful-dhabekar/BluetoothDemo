    private void startThreadConnected(BluetoothSocket socket){

        myThreadConnected = new ThreadConnected(socket);
        myThreadConnected.start();
    }

    /*
    ThreadConnectBTdevice:
    Background Thread to handle BlueTooth connecting
    */
    private class ThreadConnectBTdevice extends Thread {

        private BluetoothSocket bluetoothSocket = null;
        private final BluetoothDevice bluetoothDevice;


        private ThreadConnectBTdevice(BluetoothDevice device) {
            bluetoothDevice = device;

            try {
                bluetoothSocket = device.createRfcommSocketToServiceRecord(myUUID);
                textStatus.setText("bluetoothSocket: \n" + bluetoothSocket);
            } catch (IOException e) {
                // TODO Auto-generated catch block
                e.printStackTrace();
            }
        }

        @Override
        public void run() {
            boolean success = false;
            try {
                bluetoothSocket.connect();
                success = true;
            } catch (IOException e) {
                e.printStackTrace();

                final String eMessage = e.getMessage();
                runOnUiThread(new Runnable() {

                    @Override
                    public void run() {
                        textStatus.setText("something wrong bluetoothSocket.connect(): \n" + eMessage);
                    }
                });

                try {
                    bluetoothSocket.close();
                } catch (IOException e1) {
                    // TODO Auto-generated catch block
                    e1.printStackTrace();
                }
            }

            if(success){
                //connect successful
                final String msgconnected = "connect successful:\n"
                        + "BluetoothSocket: " + bluetoothSocket + "\n"
                        + "BluetoothDevice: " + bluetoothDevice;

                runOnUiThread(new Runnable(){

                    @Override
                    public void run() {
                        textStatus.setText(msgconnected);

                        listViewPairedDevice.setVisibility(View.GONE);
//                        inputPane.setVisibility(View.VISIBLE);
                    }});

                startThreadConnected(bluetoothSocket);
            }else{
                //fail
            }
        }

        public void cancel() {

            Toast.makeText(getApplicationContext(),
                    "close bluetoothSocket",
                    Toast.LENGTH_LONG).show();

            try {
                bluetoothSocket.close();
            } catch (IOException e) {
                // TODO Auto-generated catch block
                e.printStackTrace();
            }

        }

    }

    /*
    ThreadConnected:
    Background Thread to handle Bluetooth data communication
    after connected
     */
    private class ThreadConnected extends Thread {
        private final BluetoothSocket connectedBluetoothSocket;
        private final InputStream connectedInputStream;
        private final OutputStream connectedOutputStream;

        public ThreadConnected(BluetoothSocket socket) {
            connectedBluetoothSocket = socket;
            InputStream in = null;
            OutputStream out = null;

            try {
                in = socket.getInputStream();
                out = socket.getOutputStream();
            } catch (IOException e) {
                // TODO Auto-generated catch block
                e.printStackTrace();
            }

            connectedInputStream = in;
            connectedOutputStream = out;
        }

        @Override
        public void run() {
            byte[] buffer = new byte[3000];
            int bytes;
            String strRx = "";
            while (true) {
                try {

                    ////bytes = connectedInputStream.read(buffer);
                    int bytes1 =connectedInputStream.read(buffer,0,6);
                    strReceived = new String(buffer, 0, bytes1);
                    //final String strReceived = new String(buffer, 0, bytes);

                    final String msgReceived = String.valueOf(bytes1) +
                            " bytes received:\n"
                            ;
                    Log.d("MainActivity:: ","Values: "+msgReceived);
                    Log.d("MainActivity:: ","Values: "+strReceived);


                    runOnUiThread(new Runnable(){

                        @Override
                        public void run() {
                            textStatus.setText(msgReceived);

                            textViewBytes.setText(strReceived);

                            if(msgReceived.length() > 50){

                                Log.d("MainActivity:: ","Values: "+msgReceived);


                                tempValue = strReceived.substring(0,1);
                                temp.setText(strReceived.substring(0,1));
                                Log.d("MainActivity:: ","Temperature: "+msgReceived);

                                ldrValue = strReceived.substring(2,4);

                                ldr.setText("LDR: "+ strReceived.substring(5,5));
                                Log.d("MainActivity:: ","ldr: "+ strReceived.substring(2,4));

                                gasValue = strReceived.substring(5,5);
                                gas.setText("Gas Sensor: "+ strReceived.substring(2,4));
                                Log.d("MainActivity:: ","Gas Sensor:: "+ strReceived.substring(5,5));


                                Calendar c = Calendar.getInstance();
                                System.out.println("Current time =&gt; "+c.getTime());

                                SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                                String formattedDate = df.format(c.getTime());

                                ValueDetails valueDetails = new ValueDetails(tempValue,ldrValue,gasValue, formattedDate);
                                new AsyncInsertValues().execute(valueDetails);

                            }
                        }});

                } catch (IOException e) {
                    // TODO Auto-generated catch block
                    e.printStackTrace();

                    final String msgConnectionLost = "Connection lost:\n"
                            + e.getMessage();
                    runOnUiThread(new Runnable(){

                        @Override
                        public void run() {
                            textStatus.setText(msgConnectionLost);
                        }});
                }
            }
        }

        public void write(byte[] buffer) {
            try {
                connectedOutputStream.write(buffer);
            } catch (IOException e) {
                // TODO Auto-generated catch block
                e.printStackTrace();
            }
        }

        public void cancel() {
            try {
                connectedBluetoothSocket.close();
            } catch (IOException e) {
                // TODO Auto-generated catch block
                e.printStackTrace();
            }
        }

    }

    protected class AsyncInsertValues extends
            AsyncTask<ValueDetails, Void, Void> {
        @Override
        protected Void doInBackground(ValueDetails... params) {
            RestAPI api = new RestAPI();
            try{
                api.InsertNewValues(params[0].getTemp(),params[0].getLdr(),
                        params[0].getGas(), params[0].getDateTime());
            }
            catch (Exception e) {
                e.printStackTrace();
                Log.d("AsyncInsertValues", e.getMessage());
            }
            return null;
        }
    }

